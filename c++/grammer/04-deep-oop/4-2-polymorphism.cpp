// 상속을 쓰는 이유 - 코드 중복 방지 (공통된 속성을 부모에게 몰아넣기)
// 다형성을 쓰는 이유 - 확장성 (부모 타입 포인터 하나로 수십 개의 자식 객체를 일관되게 관리하기)

// 부모 소멸자 virtual을 안붙이면 메모리 누수
// 순수 가상함수가 포함된 클래스로 객체를 직접 못만듬
// 오버로딩과 오버라이딩의 차이는 매개변수차이 VS 재정의 차이
#include <iostream>
#include <string>

using namespace std;

class Shape {                                    // 추상 클래스 (부모)
public:
    virtual void draw() = 0;                     // 1. 순수 가상 함수: 자식은 무조건 'draw'를 직접 구현해야 함 (강제)

    virtual void printInfo() {                   // 2. 일반 가상 함수: 부모가 기본 기능을 제공하고, 자식이 '선택'적으로 재정의
        cout << "이것은 도형입니다." << endl;
    }

    virtual ~Shape() {                           // 3. 가상 소멸자: 상속 관계에서 메모리 누수를 방지하는 안전장치
        cout << "~Shape 소멸자 (부모 메모리 해제)" << endl;
    }
};

class Circle : public Shape {
public:
    void draw() override {                      // 부모의 순수 가상 함수 구현 (필수)
        cout << "원을 그립니다. " << endl;
    }
    void printInfo() override {                  // 부모의 일반 가상 함수 재정의 (선택)
        cout << "이것은 원(Circle)입니다." << endl;
    }

    ~Circle() {
        cout << "~Circle 소멸자 (자식 메모리 해제)" << endl;
    }
};

int main() {
    Shape* myShape = new Circle();              // 다형성: 부모 타입 포인터로 자식 객체 관리

    cout << "다형성 실행 " << endl;
    myShape->draw();                            // 자식의 draw() 호출
    myShape->printInfo();                       // 자식의 printInfo() 호출

    cout << "\n메모리 해제" << endl;
    delete myShape;                             // 가상 소멸자 덕분에 자식->부모 순서로 안전하게 소멸

    return 0;
}

/*
 * [4-2. 오버라이딩(Overriding) 핵심 노트]
 *
 * 1. 오버라이딩(Overriding)
 * - 상속 관계에서 부모의 함수를 똑같은 형태(이름, 매개변수)로 다시 만듦.
 * - 'virtual'과 'override' 키워드를 사용하여 다형성을 구현함.
 *
 * 2. 오버로딩(Overloading)
 * - 같은 이름의 함수를 매개변수 개수나 타입만 다르게 하여 여러 개 만듦.
 * - 상속과 상관없이 한 클래스 내부에서도 가능함.
 *
 * 3. 동적 바인딩 (Dynamic Binding)
 * - 실행 시점(Runtime)에 실제 어떤 객체인지 확인하고 그에 맞는 함수를 찾아 호출함.
 */
