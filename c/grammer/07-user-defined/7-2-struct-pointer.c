/*
 * 주제: 7-2. 구조체 포인터 (struct Pointer)
 * 날짜: 2026-04-02
 * 핵심: 점(.) 대신 화살표(->)를 써서 주소로 구조체 멤버에 접근하기
 */

#include <stdio.h>

struct Student {
    char name[20];
    int age;
};

int main() {
    struct Student s1 = {"홍길동", 20};
    struct Student *ptr = &s1; //            구조체 변수의 주소를 포인터에 대입

    printf("--- 구조체 포인터 접근 방법 ---\n");
    printf("방법 A (*ptr).age : %d\n", (*ptr).age);        // 방법 A: 정석 (포인터가 가리키는 곳에 가서 점을 찍음)
                                                           // *ptr을 괄호로 감싸야 함 (*ptr.age라고 쓰면 연산 우선순위 때문에 에러!) 
                                                           // .이 *보다 우선순위가 높으므로 반드시 ()로 ptr을 먼저 역참조해야 함
     
    printf("방법 B ptr->age   : %d\n", ptr->age);          // 방법 B: 화살표 연산자, 화살표(->)는 포인터 변수에서만 사용 가능! (s1->age는 에러)
    printf("방법 B ptr->name  : %s\n", ptr->name);         // ptr이 가리키는 구조체의 멤버로 바로 침투!

    // 포인터를 통한 데이터 수정
    ptr->age = 25;
    printf("\n--- 포인터로 수정한 후 ---\n");
    printf("s1.age : %d (ptr->age로 수정됨)\n", s1.age);

    return 0;
}

// [나중을 위한 핵심 노트]
// 1. 구조체 변수일 때는 마침표(.)를 사용 (예: s1.age)
// 2. 구조체 주소(포인터)일 때는 화살표(->)를 사용 (예: ptr->age)
// 3. ptr->age는 (*ptr).age와 완전히 같은 의미임 (단지 쓰기 편하게 만든 것뿐!)
// 4. 화살표 연산자는 오직 포인터 변수 뒤에만 붙을 수 있음

// [중첩 구조체 접근 공식 - "왼쪽 녀석의 정체만 봐라!"]
// 1. 점(.) : 내 손에 실제 데이터가 통째로 있을 때
// 2. 화살표(->) : 내 손에 주소값(포인터)만 있을 때

// 예시 1) s1.birth.year
// - s1은 변수(실체)네? -> 점(.) 찍고 들어감
// - birth도 변수(실체)네? -> 또 점(.) 찍고 들어감

// 예시 2) ptr->birth.year
// - ptr은 포인터(주소)네? -> 화살표(->) 타고 날아감
// - 도착해보니 birth는 변수(실체)네? -> 점(.) 찍고 들어감

// 기호를 나눠 놓은 이유는 = 지금 바로 여기서 찾아라고 할때는 (.) 저기 주소 적힌 데로 가서 찾아와라고 시킬 때는 (->)
                          



