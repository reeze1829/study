/*
 * 주제: 5-1. 1차원 배열 (1D Array) 심화 실습
 * 날짜: 2026-03-14
 * 내용: 
 * 1. 데이터 검색: 배열을 전수 조사하여 최댓값(Max)과 최솟값(Min) 찾기
 * 2. 데이터 가공: 특정 인덱스의 값을 수정하고 결과 확인
 * 3. 주의사항: 인덱스 범위(0 ~ Size-1)를 벗어나면 쓰레기값(Garbage Value) 발생
 */

// 1차원 배열 - 구조적강조 : 같은 자료형의 변수들을 기차 칸처럼 한 줄로 늘어뜨린 데이터 그룹으로, 하나의 이름과 방 번호(인덱스)로 관리하는 구조
//            - 메모리강조 : 데이터들이 메모리상에 빈틈없이 연속적으로 붙어 있어, 인덱스 번호만으로 원하는 위치에 즉시 접근할 수 있는 선형 자료구조
#include <stdio.h>

int main() {
    double weights[5] = {65.5, 72.3, 58.9, 88.2, 70.1};         // 배열 선언 및 초기화 (학생 5명의 몸무게 데이터)
    // 연속성: 데이터가 메모리에 빈틈없이 붙어 있기 때문에 아래 sizeof 공식이 성립함!
    int size = sizeof(weights) / sizeof(weights[0]);            // 배열의 전체 길이를 구하는 공식
    // sizeof(배열명) / sizeof(배열[0])를 사용하면 배열의 칸 수를 자동으로 계산할 수 있음!
    // (나중에 배열 크기가 바뀌어도 코드를 수정할 필요가 없어 효율적)
    // 예) double weights[10] 으로 바뀐다고하면 직접 숫자 5를 썻다고하면 코드 전체에 숫자 5를 10으로 바꿔야 
    // sizeof 을 쓰면 : 80(전체 바이트)/8(더블형 자료형변수) 이라고 자동으로 size 변수 업데이트

    printf("--- 현재 등록된 몸무게 데이터 (%d명) ---\n", size);
    for (int i = 0; i < size; i++) {
        printf("[%d번 방]: %.1f kg\n", i, weights[i]);
    }
    // 최댓값, 최솟값 찾기 (선형 탐색 알고리즘)
    double max = weights[0];                                    // 일단 0번 방이 제일 크다고 가정 ,  0번을 기준으로 하나씩 비교하며 교체          
    double min = weights[0];                                    // 일단 0번 방이 제일 작다고 가정

    for (int i = 1; i < size; i++) {   
        if (weights[i] > max) max = weights[i];                 // 더 큰 놈 발견하면 교체
        if (weights[i] < min) min = weights[i];                 // 더 작은 놈 발견하면 교체
    }

    printf("\n[데이터 업데이트] 2번 방(58.9kg) 학생의 몸무게가 수정됩니다.\n");   // 특정 데이터 수정
    weights[2] = 55.2; 

    printf("\n--- 분석 결과 리포트 ---\n");
    printf("최고 몸무게: %.1f kg\n", max);
    printf("최저 몸무게: %.1f kg\n", min);
    printf("수정된 2번 방 데이터: %.1f kg\n", weights[2]);

    //에외상황 - printf("범위 밖 데이터(weights[5]): %.1f\n", weights[5]);    인덱스 범위를 벗어날경우     (메모리 침범 에)

    return 0;
}
