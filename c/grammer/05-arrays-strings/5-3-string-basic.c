/*
 * 주제: 5-3. 문자열(String) 기초와 널 문자(\0)의 원리 (통합본)
 * 날짜: 2026-03-18
 * 내용: 
 * 1. 문자열 선언: char str[] = "Hello"; 의 실제 구조와 널 문자(\0)
 * 2. 문자열 수정: 배열의 인덱스를 이용해 특정 문자 변경
 * 3. 널 문자 조작: 중간에 \0을 넣었을 때 printf가 인식하는 범위 확인
 */

#define _CRT_SECURE_NO_WARNINGS // Visual Studio scanf 에러 방지용
#include <stdio.h>

int main() {
                                                    // 문자열 기본 구조 확인
    char greeting[] = "Hello";                      // 문자열 선언 (H, e, l, l, o + \0 = 6바이트) 마지막에 0(NULL)이 들어있는 문자들의 줄 세우기
    int size = (int)sizeof(greeting);               // 형 변환으로 경고 방지, sizeof로 배열 전체 크기(6)를 구하고, int형으로 변환하여 저장
  
    printf("--- 문자열 기본 정보 ---\n");
    printf("문자열 내용: %s\n", greeting);              // %s 서식 지정자로 문자열 전체 출력
    printf("메모리 점유 크기: %d 바이트\n", size);      // 널 문자(\0)를 포함한 전체 메모리 크기 출력

    printf("\n--- 한 글자씩 인덱스로 접근 ---\n");      // 한 글자씩 출력하며 널 문자 확인 시작
    for (int i = 0; i < size; i++) {                   // 배열의 전체 크기(6)만큼 반복 루프 실행
        if (greeting[i] == '\0') {                     // 만약 현재 칸의 문자가 널 문자('\0')라면
            printf("인덱스 [%d]: \\0 (널 문자 발견!)\n", i);  // 널 문자의 위치를 시각적으로 표시
        } else {                                              // 일반 문자라면
            printf("인덱스 [%d]: %c\n", i, greeting[i]);      // 해당 인덱스의 문자를 하나씩 출력
        }
    }

                                                           // str 변수를 활용한 수정 및 조작 실습
    printf("\n--- 문자열 수정 및 널 문자 조작 ---\n");
    char str[] = "Apple Pie";                              // 새로운 문자열 선언 (공백 포함 9글자 + \0 = 10바이트)
    printf("원본 문자열: %s\n", str);

    str[0] = 'O';                                          // 'A' -> 'O'로 수정 (배열이므로 인덱스 접근 후 값 변경 가능)
    printf("1차 수정(Opple Pie): %s\n", str);

    str[5] = '\0';                                         // 널 문자(\0)를 중간(5번 인덱스(공백 자리))에 강제 삽입하여 문자열을 논리적으로 자르기 
    printf("2차 수정(널 문자 삽입): %s\n", str);           // 출력 결과 "Opple" (printf는 \0을 만나면 뒤에 데이터가 있어도 종료함)

                                                           // 한글 입력 및 보안 실습
    char name[40];                                         // 안전한 한글 입력을 위해 넉넉한 크기(40바이트)로 선언
    printf("\n--- 이름 입력 (한글 가능) ---\n");
    printf("이름을 입력하세요: ");
    scanf("%39s", name);                                   // %39s: 배열 크기보다 1 작게 설정하여 버퍼 오버플로우 방지 및 \0 자리 확보
                                                           // 마지막 한 칸(\0)을 비워두지 않으면 널 문자가 옆집(인접 메모리)을 침범해 프로그램이 죽거나 해킹(Buffer Overflow)에 노출
    
    // 한글은 1글자당 2~3바이트를 차지함
    // char name[10]에 안녕하세요를 넣으면 널 문자(\0) 자리가 없어서 글자가 깨짐(Overflow)
    // 한글 입력을 위해서는 배열 크기를 넉넉히(예: 40) 잡아야 함
    printf("반갑습니다, %s님!\n", name);
    // scanf는 공백(띄어쓰기)을 입력 종료로 인식함. 홍 길동 입력 시 홍만 저장됨
    // strlen() 함수를 쓰면 널 문자(\0)를 제외한 실제 글자 수만 구할 수 있음
    return 0;
}
