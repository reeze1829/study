/*
 * 주제: 3-3. for 반복문과 흐름 제어 (for-loop)
 * 날짜: 2026-03-02
 * 내용:
 * 1. for문의 실행 순서: 1(초기식) -> [ 2(조건식) -> 3(반복코드) -> 4(증감식) ] 순환 구조 이해
 * 2. 초기식의 특성: 루프 진입 시 단 1회만 실행되며, 이후에는 (2-3-4) 과정만 무한 반복됨
 * 3. 입구 컷 원리: 조건식(2)이 처음부터 거짓(False)이면, 중괄호 안의 코드(3)는 단 한 번도 실행되지 않음
 * 4. 흐름 제어의 차이: break(현재 루프 즉시 완전 탈출), continue(현재 바퀴 중단 후 즉시 증감식(4)으로 점프)
 * 5. 변수의 사후 상태: 루프 종료 후 제어 변수(k)는 조건식에서 '거짓' 판정을 받은 직후의 값을 유지함
 */
#include <stdio.h>

int main() {
    printf("--- 정방향 반복 ---\n");     // 1부터 5까지 정방향 출력                                         // 증감식은 중괄호 안의 코드가 '모두 실행된 후'에 호출됨!
    for (int i = 1; i <= 5; i++) {                                                                         //1단계(초기식) -2단계(조건식)- 3단계(반복코드)- 4단계(증감) 1->(2->3->4) 구조 
        printf("%d ", i);                                                                                  //1단계는 한번만 실행되고 그 다음은 234 반복!(1로 가면 무한루프로 빠짐 )
    }
    printf("\n");
  
    printf("\n--- 역순 반복 ---\n");     // 5부터 1까지 역순 출력 (i-- 활용)                    
    for (int i = 5; i >= 1; i--) {      // i가 1보다 크거나 같은 동안 1씩 감소
        printf("%d ", i);
    }
    printf("\n");
  
    printf("\n--- 누적 합산 (1~10) ---\n");     // 1부터 10까지의 합 구하기 (누적 연산)
    int sum = 0;                                // 합계를 저장할 변수는 반드시 0으로 초기화!
    for (int i = 1; i <= 10; i++) {
        sum += i;                               // sum = sum + i;
        printf("%d 더하는 중... 현재 합계: %d\n", i, sum);
    }
    printf("최종 결과: %d\n", sum);

    printf("\n--- continue & break 테스트 ---\n");    // continue: 주로 예외 처리 시 사용, 증감식으로 점프! break: 무한 루프 탈출 시 사용, 중괄호 밖으로 완전히 탈출!!
    for (int i = 1; i <= 10; i++) {
        if (i % 2 == 0) continue;                     // 짝수면 아래 코드를 무시하고 증감식(i++)으로 점프
        if (i > 7) break;                             // i가 7보다 커지면 반복문 즉시 종료
        printf("%d ", i);                             // 결과: 1 3 5 7
    }
    printf("\n");

    printf("\n--- 두 변수 동시 제어 ---\n");            // 다중 증감식 (콤마 연산자) 
    for (int i = 1, j = 10; i <= j; i++, j--) {        // i는 1부터 증가, j는 10부터 감소하며 서로 만날 때까지 반복
        printf("i:%d, j:%d\n", i, j);
    }

    printf("\n--- 이중 for문: 구구단 (2~3단) ---\n");
    for (int i = 2; i <= 3; i++) {         // 바깥쪽 루프: 단수 (2단, 3단)    // 바깥쪽 for문: 세로축(행, Row)을 결정함!!!
        printf(" [%d단 시작]\n", i);
        for (int j = 1; j <= 9; j++) {     // 안쪽 루프: 곱해지는 수 (1~9)    // 안쪽 for문: 가로축(열, Column)을 결정함!  // 안쪽 루프가 완전히 종료되어야 바깥쪽 루프의 증감식으로 이동함!!
            printf("  %d x %d = %d\n", i, j, i * j);                         // 핵심: 안쪽 루프는 바깥쪽 루프의 상태에 종속되어 유동적으로 움직일 수 있음!
        }                                                                    
        printf("\n"); 
    }
    printf("\n--- 이중 루프 break 테스트 ---\n");
    for (int i = 1; i <= 3; i++) {
        for (int j = 1; j <= 3; j++) {
            if (i == 2 && j == 2) {                                      // j=2: if (i == 2 && j == 2) 조건이 딱 걸려서 (2,2), (2,3)은 출력되지 않음 
                printf("  [break!] ");                                   // 안쪽 루프에서 break을 걸면 현재 속해있는 안쪽루프 딱 하나만 탈출함!!
                break;                                                   // j 루프만 탈출함. i=2일 때 j=3은 실행 안 됨. break는 현재 돌고 있는 한 판만 깨뜨리는 거지, 다음 판(i=3)까지 취소시키지는 않음!!
            }
            printf("(%d,%d) ", i, j);
        }
        printf("\n");                                                    // i 루프는 살아있어서 줄바꿈은 계속 실행됨
    }

    printf("--- 이중 for문: 별 찍기 (직각삼각형) ---\n");
    for (int i = 1; i <= 5; i++) {                                         // i는 '줄(Row)'의 번호
        for (int j = 1; j <= i; j++) {                                     // i가 1일 때 j는 1번, i가 5일 때 j는 5번 반복 -> 바깥쪽 i가 안쪽 j의 한계점을 결정함!
            printf("*");
        }
        printf("\n");
    }
    printf("\n--- 반복 종료 후 변수 상태 확인 ---\n");   //  루프 외부 변수 활용 
    int k; 
    for (k = 1; k <= 3; k++) {  
    }
    
    printf("루프 탈출 직후 k의 값은 %d입니다.\n", k); // k가 4가 되는 순간 (k <= 3)이 거짓이 되어 탈출했음을 증명

    /* 무한 루프의 형태들
    1. for (;;) {...}    <- 가장 정석적인 표현
    2. while (1) {...}  
    */
    return 0;
}
