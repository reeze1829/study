/*
 * 주제: 4-3. 변수 유효 범위와 데이터 보호 (local-global)
 * 날짜: 2026-03-10
 * 내용: 
 * 1. 지역 변수: 블록{ } 내에서만 유효, 함수 종료 시 소멸 (Call by Value 원리)
 * 2. 전역 변수: 프로그램 전체 공유, 남용 시 코드 흐름 추적이 어려워 위험함
 * 3. 우선순위: 이름 충돌 시 가장 가까운 곳의 변수가 우선(Shadowing)하며 블록 생명 주기 확인
 */

#include <stdio.h>

int num = 100;                // 전역 변수: 프로그램 전체에서 공유 (거실 TV)

void increase_num() {
    num = num + 10;           // 전역 변수 num을 직접 수정 (어디서든 접근 가능)
    printf("함수(increase_num) 안: 전역 변수 num 수정됨 -> %d\n", num);
}

void check_priority() {
    int num = 200;            // 이름이 같은 '지역 변수': 전역 변수 num을 일시적으로 가림
    printf("함수(check_priority) 안: 지역 변수 num = %d\n", num); // 현재 블록{ } 내 변수가 우선순위 높음
}

int main() {
    int local_num = 50;       // 메인 함수의 지역 변수 (내 방 물건)

    printf("초기 전역 변수 num: %d\n", num);
  
    increase_num();           // 전역 변수는 함수 호출만으로도 원본이 수정됨 (편하지만 추적이 어려움)
    printf("함수 호출 후 전역 변수 num: %d\n", num);

    check_priority();         // 이름 충돌 테스트: 지역 변수가 이기는지 확인
    printf("메인에서 본 전역 변수 num: %d (지역 변수 200은 죽고 다시 전역 110이 보임)\n", num); 
    printf("main 지역 변수 원본: %d (Call by Value 덕분에 원본 유지)\n", local_num);  // 매개변수로 전달할 때 '복사본'을 주기 때문에 메인의 local_num은 보호됨 

    {
        int block_num = 777;                // 블록 스코프 
        printf("블록 { } 안: block_num = %d\n", block_num);
    } 
    // printf("%d", block_num); // 주석 해제 시 에러 발생! 블록 밖이라서 사라졌기 때문.
    return 0;
  // 생존 범위 : 지역 변수는 중괄호 { }를 탈출하는 순간 메모리에서 즉시 소멸함.
  // 변수 가리기 : 이름이 같을 때는 가장 가까운 곳에 선언된 변수가 이김 (전역 변수보다 지역 변수가 힘이 셈)
  // 전역 변수의 위험성 : 모든 함수가 전역 변수를 건드릴 수 있기 때문에, 프로그램이 커지면 어디서 값이 바뀌었는지 찾기 힘들어짐
}

/*
 * [실행 결과 및 증명]
 * 1. 초기 100 -> 수정 후 110 : 전역 변수는 어디서든 접근 및 수정 가능 (공유)
 * 2. 함수 안 지역 200        : 이름 충돌 시 '가까운' 지역 변수가 우선순위 높음 (Shadowing)
 * 3. 메인 다시 110           : 함수가 종료되면 그 안의 지역 변수(200)는 즉시 소멸 (Life Cycle)
 * 4. 메인 지역 50            : 함수에 값을 넘겨도 복사본만 전달되어 원본은 안전 (Call by Value)
 * 5. 블록 안 777             : 중괄호 { }만으로도 독립된 생존 영역(Scope)이 생성됨
 *
 * 결론: 전역 변수는 편리하지만 위험하고, 지역 변수는 독립된 방(Scope)을 가져서 안전함!
 */
