

/*
 * 주제: 2-2. 문자와 아스키코드 (char-ascii)
 * 날짜: 2026-02-20
 * 내용: 
 * 1. char 자료형의 본질: 문자는 내부적으로 아스키코드(정수)로 저장됨을 이해
 * 2. 대소문자 변환 원리: 대문자와 소문자의 아스키코드 차이(32)를 이용한 연산
 * 3. 문자형 숫자 변환: 문자 '0'(48)을 빼서 실제 정수 값을 추출하는 테크닉 숙달
 * 4. 주요 구간 암기: 숫자('0'~'9': 48~57), 대문자('A'~'Z': 65~90), 소문자('a'~'z': 97~122)
 */
#include <stdio.h>

int main() {  
    char grade = 'A';                                       // 컴퓨터는 'A'를 보는 게 아니라 숫자 65를 봄. 그래서 문자에 더하기 빼기가 가능함
    printf("1. 기초 학습\n");
    printf("기호 출력(%%c): %c\n", grade);    // A
    printf("번호 출력(%%d): %d\n\n", grade);  // 65

    // 대소문자 변환 (연산 활용)
    char upper = 'G';
    char lower = upper + 32;             // 아스키코드에서 'a'(97)와 'A'(65)의 차이는 항상 32임
    printf("2. 대소문자 변환\n");
    printf("대문자 '%c'에 32를 더하면 소문자 '%c'가 됩니다.\n\n", upper, lower);

    char char_num = '7';                // 문자 숫자('7')를 진짜 숫자(7)로 바꾸기
    int real_num = char_num - '0';      // 문자 '0'은 아스키코드로 48임       -   문자 '7'(숫자 55)에서 문자 '0'(숫자 48)을 빼면 진짜 숫자 7이 나옴
                                        // 문자 '0'을 빼면 숫자가 됨!   
    //48~ 57 문자 0~9 , 58 :, 59 ;, 60 <, 61 =, 62 > , 63 ? , 64 @    65 ~ 90 A~Z ,  97 ~ 122 a~z
  
    printf("3. 문자형 숫자 변환\n");
    printf("문자 '%c'의 아스키코드: %d\n", char_num, char_num);
    printf("진짜 숫자 %d + 1의 결과: %d\n\n", real_num, real_num + 1);

    printf("제어 문자 체험\n");
    printf("백슬래시 t를 쓰면\t간격이 벌어집니다.\n");
    printf("백슬래시 a를 쓰면 경고음이 들릴 수도 있습니다!\a\n");

    return 0;
}
