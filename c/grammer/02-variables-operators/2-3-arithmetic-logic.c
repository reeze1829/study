/*
 * 주제: 2-3. 산술 및 논리 연산자 
 * 날짜: 2026-02-22
 * 내용:
 * 1. 산술 연산: 정수 나눗셈의 특성(소수점 절삭) 및 나머지 연산자(%)의 순환성 확인
 * 2. 증감 연산: 전위(++num)와 후위(num++)의 실행 순서 차이 이해
 * 3. 논리 연산: AND(&&), OR(||), NOT(!)의 결과값 및 '0이 아닌 숫자는 참'임을 확인
 * 4. 관계 연산: 비교 결과가 참이면 1, 거짓이면 0으로 반환됨을 확인
 */

#include <stdio.h>

int main() {
    int a = 10, b = 3;
    printf("산술 연산\n");
    printf("%d / %d의 몫: %d\n", a, b, a / b);       // 정수끼리의 나눗셈은 소수점을 버림!!
    printf("%d / %d의 나머지: %d\n\n", a, b, a % b); // 홀수/짝수 판별 등에 쓰임              

    int num = 5;
    printf("증감 연산\n");
    printf("현재 값: %d\n", num++); // 출력하고 나서 1 증가 (후위)
    printf("증가 후: %d\n", ++num); // 1 증가시키고 출력 (전위)
    printf("\n");

    int x = 10, y = 20;
    printf("논리 연산\n");
    printf("x가 y보다 작고, x는 10인가? : %d\n", (x < y) && (x == 10)); // 1 (True)
    printf("x가 15보다 크거나, y가 15보다 큰가? : %d\n", (x > 15) || (y > 15)); // 1 (True)
    printf("x는 10이 아닌가? : %d\n", !(x == 10)); // 0 (False)
    printf("1 && 0의 결과: %d\n", 1 && 0); // 0 출력     And(&&)는 둘다 1이어야 1을 반환함!!
    printf("1 || 0의 결과: %d\n", 1 || 0); // 1 출력         5 || 0 이어도 1이 반환 - 0이아닌 숫자는 모두 참으로 인식 참은 1 거짓은 0

    printf("관계 연산\n");
    printf("10이 10과 같은가? : %d\n", 10 == 10); // == 는 '같다'
    printf("10이 10과 다른가? : %d\n\n", 10 != 10); // != 는 '다르다'

    printf("나머지 연산 활용\n");     // 어떤 수를 N으로 나눈 나머지는 항상 0 ~ (N-1) 사이만 나옴!!
    printf("10 %% 3 = %d\n", 10 % 3); // % 기호를 출력하려면 %%라고 써야 함!
    printf("11 %% 3 = %d\n", 11 % 3);
    printf("12 %% 3 = %d\n", 12 % 3); // 다시 0으로 돌아옴
    return 0;
}
