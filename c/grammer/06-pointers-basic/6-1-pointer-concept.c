/*
 * 주제: 6-1. 포인터 기초 개념 (주소와 참조)
 * 날짜: 2026-03-22
 * 내용: 
 *  - & (Ampersand): 주소 연산자 (변수의 메모리 번지를 추출)
 *  - * (Asterisk): 간접 참조 연산자 (해당 주소로 가서 알맹이를 확인/수정)
 *  - 포인터 변수의 크기와 자료형의 관계 이해
 */

#include <stdio.h>

int main() {
                                       // 일반 변수 선언 (메모리 상에 공간 확보)
    int unit_hp = 100;                 // 유닛의 체력
    int bonus = 50;                    // 보너스 체력
    
                                       // 포인터 변수 선언 (주소값(호수)을 담을 수 있는 '특수 바구니')
                                       // 선언 시의 *는 "이 변수는 주소를 담는 포인터입니다"라는 선언임
    int* ptr = NULL;                   // 초기값은 안전하게 NULL(0)로 설정 
                                       // int* ptr (포인터 선언): 주소는 일반 정수(int)와 섞이면 위험하므로, 반드시 이건 주소를 담는 변수라고 *를 붙여서 따로 관리함!
    printf("=== 변수의 주소 확인 (&) ===\n");
    printf("unit_hp의 값: %d\n", unit_hp);
    printf("unit_hp의 실제 메모리 주소: %p\n", (void*)&unit_hp);
    printf("bonus의 실제 메모리 주소:   %p\n", (void*)&bonus);

    printf("\n=== 포인터와 변수 연결 ===\n");                 // ptr이 unit_hp의 주소를 가리키게 함
    ptr = &unit_hp;                                          // &unit_hp(주소 확인): 컴퓨터 메모리(RAM)는 거대한 아파트 단지! &는 그 아파트의 동/호수를 확인하는 작업!
    printf("ptr에 저장된 주소값: %p\n", (void*)ptr);
    printf("ptr 자체의 메모리 주소: %p\n", (void*)&ptr);      // 포인터도 변수라 자기 주소가 있음!

    printf("\n=== 간접 참조를 통한 값 읽기 (*) ===\n");
    printf("ptr이 가리키는 곳의 값: %d\n", *ptr);             // *ptr은 "ptr에 적힌 주소로 가서 거기 든 값을 가져와라"는 명령

    printf("\n=== 포인터를 이용한 원격 조종 ===\n");
    printf("변경 전 unit_hp: %d\n", unit_hp);
    
    *ptr = 200;                                              // unit_hp를 직접 건드리지 않고, 주소로 가서 수정함 
    printf("ptr을 통해 수정한 후 unit_hp: %d\n", unit_hp);   

    printf("\n=== 가리키는 대상 변경 ===\n");
    ptr = &bonus;                                           // 이제 ptr은 bonus 변수의 주소를 가짐
    *ptr = *ptr + 100;                                      // bonus의 주소로 가서 값을 100 증가시킴
    printf("변경 후 bonus 값: %d\n", bonus);                // *ptr (역참조): 내가 직접 unit_hp라는 이름을 부르지 않아도, 주소만 알면 값을 바꿀 수 있는 원격 조종 장치!!
    
    return 0;
}
// void* 캐스팅: %p로 출력할 때 (void*)를 붙이는 이유는 이건 어떤 타입이든 상관없는 순수한 주소값이다라고 컴파일러에게 알려주는 안전장치임!!

// & (앤) = 주소 따기(위치 파악) : 변수 이름 앞에 &를 붙이는 건, 그 변수가 메모리 어디에 사는지 집 주소만 알아내는 행동
//                      &hp : hp야, 너네 집 몇 호니? ->  저 101호 살아요 (결과값: 101)
//                      이건 값을 바꾸는 게 아니라, 그냥 어디 있는지 확인만 하는 거임
// * (별) = 주소로 쳐들어가기(실제 조작) : 포인터 변수 앞에 *를 붙이는 건, 손에 든 주소를 보고 그 집으로 직접 쳐들어가서 안에 있는 사람을 만나는 행동
//                                 *ptr : "메모지에 적힌 101호로 당장 들어가!" -> 들어가서 그 안의 값을 바꾸거나 읽음
//                                 이게 바로 원격 조종의 핵심


