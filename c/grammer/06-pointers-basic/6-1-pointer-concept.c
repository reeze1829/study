// int* ptr;          // 2. 주소만 적을 수 있는 빈 메모지 준비
// ptr = &unit_hp;    // 3. [앤(&)으로 주소 따기] unit_hp네 집 주소를 메모지에 적음
// *ptr = 200;        // 4. [별(*)로 주소 쳐들어가기] 메모지에 적힌 주소로 가서 
                      //    안에 있는 unit_hp의 몸무게를 200으로 바꿔버림!

// *** 포인터를 사용하는 이유는? *** 메모리를 효율적으로 쓰고, 멀리 떨어진 데이터를 직접 조종하기 위해서(원격조종, 효율성, 동적할당)
// 원격 조종을 해하는 이유는?  - 함수 사이의 벽을 넘기 위해서, "복사 비용"을 아끼기 위해서 (성능 문제), 데이터의 "생존 범위"를 넓히기 위해서 -
// C언어에서 각 함수는 자기만의 독립된 방(메모리 공간)을 가지고 있음
// - 포인터가 없을 때(값의 복사) = 원본은 가만히 두고, 똑같은 내용의 "가짜 복사본"을 메모리 다른 칸에 하나 더 만
// - 포인터가 있을 때(위치의 공유) = 데이터를 새로 만들지 않고, 원본이 저장된 "실제 메모리 칸의 좌표"를 그대로 사용함
// 포인터는 원본을 새로 만들지 않고, 이미 존재하는 원본의 좌표를 재사용하여 메모리를 직접 타격하는 수단임

//                      원격 조종 안 할 때 (값 복사)                     원격 조종 할 때 (포인터)
// 속도                 느림 (매번 복사해야 함)                          매우 빠름 (주소만 전달)
// 영향력               원본을 바꿀 수 없음                              원본을 직접 수정 가능
// 메모리               복사본만큼 추가 소모                             추가 소모 거의 없음

// 나무위키를 예로 들면 각주번호[1] = 이게 포인터!! 본문(현재 함수)에 데이터 전체를 다 적지 않고, 자세한건 거기 밑에서 봐라고 위치만 알려줌
// 클릭하는 동작: 이건 역참조!  번호를 누르는 순간, 그 번호가 가리키는 실제 설명이 있는 곳(메모리 주소)으로 이동해서 내용을 읽음
// 맨 밑의 상세 설명: 이게 바로 실제 변수의 값


