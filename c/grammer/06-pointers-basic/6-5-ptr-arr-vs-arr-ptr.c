/*
 * 주제: 6-5. 포인터 배열 vs 배열 포인터
 * 날짜: 2026-03-30
 * 핵심: 이름의 마지막 단어가 진짜 정체다!
 */

#include <stdio.h>

int main() {
    int a = 10, b = 20, c = 30;
    int arr[3] = {1, 2, 3};

    // --- 포인터 배열 (Pointer Array) ---
    int* ptr_arr[3] = {&a, &b, &c}; // 정체: "포인터들을 담은 배열" (주소 전용 보관함)
                                    // 활용: 여러 개의 문자열(이름 목록 등)을 관리할 때 주로 사용함!
    printf("--- 포인터 배열 (Pointer Array) ---\n");
    for (int i = 0; i < 3; i++) {
        printf("ptr_arr[%d]가 가리키는 값: %d\n", i, *ptr_arr[i]);
    }

    // 문자열 목록 관리할 때의 모습
    char* snacks[3] = {"Apple", "Banana", "Candy"};
    printf("포인터 배열 활용(문자열): %s, %s\n", snacks[0], snacks[1]);


    // --- 배열 포인터 (Array Pointer) ---
    int (*arr_ptr)[3] = &arr;       // 정체: "배열 하나를 통째로 가리키는 포인터" (배열 전용 리모컨)
                                    // 가로()가 반드시 있어야 함! 없으면 포인터 배열이 됨
                                    // 활용: 2차원 배열을 함수의 인자로 넘길 때 필수적으로 사용함!
    printf("\n--- 배열 포인터 (Array Pointer) ---\n");
    printf("arr_ptr을 통해 본 첫 번째 값: %d\n", (*arr_ptr)[0]);
    printf("arr_ptr을 통해 본 두 번째 값: %d\n", (*arr_ptr)[1]);

    // 2차원 배열 핸들링할 때의 모습
    int matrix[2][3] = {{10, 20, 30}, {40, 50, 60}};
    int (*mat_ptr)[3] = matrix;     // 2차원 배열의 한 줄씩 가리킴
    printf("배열 포인터 활용(2차원): %d\n", mat_ptr[1][0]); // 40 출력


    // --- 메모리 크기 확인 ---
    printf("\n--- 메모리 점유 확인 ---\n");
    printf("ptr_arr의 크기: %zu 바이트\n", sizeof(ptr_arr)); // 주소 3개 크기
    printf("arr_ptr의 크기 : %zu 바이트\n", sizeof(arr_ptr));  // 주소 1개 크기

    return 0;
}

// [나중을 위한 핵심 요약]
// 1. 포인터 배열 (int* ptr[3]): "여러 개의 주소"를 보관하는 아파트 (각 층에 주소가 삼)
// 2. 배열 포인터 (int (*ptr)[3]): "배열 한 덩어리"를 가리키는 커다란 리모컨
// 3. 꿀팁: 괄호()가 있으면 포인터가 주인공(배열 포인터), 없으면 배열이 주인공(포인터 배열)!
// 4. 결정적 차이: ptr_arr은 주소 3개를 담는 공간이 필요하지만, arr_ptr은 주소 딱 1개만 담는 변수임
// 5. 실전 매칭: 포인터 배열 = 문자열 여러 개 / 배열 포인터 = 2차원 배열 관리용
